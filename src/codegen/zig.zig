fn primitiveTypeName(p: IR.PrimitiveType) []const u8 {
    return switch (p) {
        .u8 => "u8",
        .u16 => "u16",
        .u32 => "u32",
        .u64 => "u64",
        .i8 => "i8",
        .i16 => "i16",
        .i32 => "i32",
        .i64 => "i64",
        .f32 => "f32",
        .f64 => "f64",
        .bool => "bool",
        .varint => "varint",
    };
}

fn endianLiteral(e: IR.Endian) []const u8 {
    return switch (e) {
        .big => ".big",
        .little => ".little",
    };
}
